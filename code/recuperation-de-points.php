		<p>Vous avez été verbalisé et avez reçu du Ministère de l’intérieur un courrier vous informant de la perte de vos points. Ce document porte la référence 48. Ce stage vous permettra de récupérer 4 points sans pouvoir dépasser un total, à l’issue du stage, de 12 points (6 en période probatoire). Ces points sont inscrits au fichier national du permis de conduire en retenant comme date de valeur le lendemain du deuxième jour du stage. Il existe certaines conditions pour pouvoir récupérer des points à la suite de ce stage:
– Disposer au minimum d’un point sur votre permis de conduire.
– Ne pas avoir récupéré de points à la suite d’un stage analogue s’étant déroulé moins de 1 ans avant le stage.
– A voir effectivement perdu des points à la date du stage.

Il existe des délais entre la perte de points et son imputation sur le fichier national puis le courrier en informant le conducteur. Ainsi un conducteur peut avoir perdu juridiquement l’ensemble de ses points sans que cela ne soit encore inscrit sur le fichier national. C’est le capital points, tel qu’il figure au fichier national des permis de conduire, qui fait foi. Avant de s’inscrire à un stage, si vous n’avez pas reçu de document référence 48, vous êtes invité à vous rendre dans la préfecture ou la sous-préfecture de votre choix (muni d’une pièce d’identité) pour vérifier qu’une perte de points a déjà été enregistrée sur le fichier national.

Lorsque le capital point devient égal ou inférieur à 0, le Ministère de l’intérieur envoie au titulaire du permis de conduire une lettre de notification référence 48S par courrier recommandé avec accusé réception. Dans ce cas, les points relatifs à un stage sont affectés au permis de conduire dès lors que le premier jour du stage a eu lieu avant la date de réception du courrier ou avant que le délai pour retirer le courrier recommandé ne soit épuisé</p>
